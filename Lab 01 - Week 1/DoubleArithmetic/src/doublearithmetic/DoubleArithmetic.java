public class DoubleArithmetic {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double a = 1.5;
        double b = 2.5;
        
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
    
}
